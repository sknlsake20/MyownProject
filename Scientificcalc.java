package Task5;

public class Scientificcalc {
	public static void main(String[] args) {
	   System.out.println(Math.sqrt(25)+" is a squareroot value");
	   System.out.println(Math.cos(30)+" is a cos value");
	   System.out.println(Math.sin(45)+" is a sin value");
	   System.out.println(Math.tan(60)+" is a tan value");
	   System.out.println(Math.log(10)+" is a log value");
	   System.out.println(Math.max(89,102)+" is a max value");
	   System.out.println(Math.min(80.12, 80.90)+" is a min value");
	   System.out.println(Math.pow(28,4)+" is a pow value");
	   System.out.println(Math.log1p(28)+" is a log1p value");
	   System.out.println(Math.exp(28)+" is a exp value");
	   System.out.println(Math.sinh(30)+" is a sinh value");
	   System.out.println(Math.cosh(30)+" is a cosh value");
	   System.out.println(Math.tanh(30)+" is a tanh value");
	   System.out.println(Math.round(2.89710)+" is a round value");
	   System.out.println(Math.ceil(3)+" is a ceil value");
	}
}
